	public function greater_than_field($str, $field)
	{
		if ( ! isset($_POST[$field]))
			return FALSE;
		if ( ! is_numeric($str))
			return FALSE;
		$min = $_POST[$field];
		return $str > $min;
	}

	public function not_greater_than_field($str, $field)
	{
		if ( ! isset($_POST[$field]))
			return FALSE;
		if ( ! is_numeric($str))
			return FALSE;
		$min = $_POST[$field];
		return $str <= $min;
	}

	public function less_than_field($str, $field)
	{
		if ( ! isset($_POST[$field]))
			return FALSE;
		if ( ! is_numeric($str))
			return FALSE;
		$max = $_POST[$field];
		return $str < $max;
	}

	public function not_less_than_field($str, $field)
	{
		if ( ! isset($_POST[$field]))
			return FALSE;
		if ( ! is_numeric($str))
			return FALSE;
		$min = $_POST[$field];
		return $str >= $min;
	}

	public function differs($str, $field)
	{
		return ! $this->matches($str, $field);
	}
